package com.jetpack.demos.generic.intro;

/**
 * @author wzc
 * @date 2019/2/18
 */
public class DoublePoint {
    private double x;
    private double y;

    public DoublePoint() {
    }

    public DoublePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
