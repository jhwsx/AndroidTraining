package com.jetpack.demos.generic.intro;

/**
 * @author wzc
 * @date 2019/2/18
 */
public class IntegerPoint {
    private int x;
    private int y;

    public IntegerPoint() {
    }

    public IntegerPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
